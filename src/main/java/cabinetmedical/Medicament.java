package cabinetmedical;

public class Medicament {
    private String nom;
    private String posologie;

    
    
    public Medicament(String nom, String posologie) {
        this.nom = nom;
        this.posologie = posologie;
    }
    public String getNom() {
        return nom;
    }
    public String getPosologie() {
        return posologie;
    }
    
    

    
}

